    SELECT tip_comanda INTO tip 
    FROM COMANDA
    WHERE COMANDA.id_comanda = :new.id_comanda;
    IF tip = 'Depozitare' 
        THEN
        UPDATE PRODUS_STOC
        SET nr_paleti = nr_paleti + :new.nr_paleti
        WHERE id_produs= :new.id_produs;
    ELSE
        UPDATE PRODUS_STOC
        SET nr_paleti = nr_paleti - :new.nr_paleti
        WHERE id_produs= :new.id_produs;
    END IF;
END Update_stock;
